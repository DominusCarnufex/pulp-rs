#include <stdio.h>

struct HeapVar  {
    const unsigned char *pointer;
    size_t size;
};

extern struct HeapVar version_c(struct HeapVar);

int main()  {
    unsigned char bytecode[0x4e] = {
        0x50, 0x55, 0x4c, 0x50,
        0x00, 0x01, 0x00,
        0x00,
        0x57, 0x6e, 0xbc, 0xfa,
        0x32, 0x00, 0x7b, 0xf9, 0x73, 0xca, 0x8b, 0x5f,
        0x09, 0xe0, 0x54, 0x09, 0x3a, 0xab, 0xf2, 0x60,
        0x32, 0x00, 0x00, 0x00,
        0x01, 0x00, 0x00, 0x00,
        0x04, 0x6d, 0x61, 0x69, 0x6e,
        0x04, 0x00, 0x00, 0x00,
        0x16, 0x00, 0x02, 0x00,
          0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
          0x01, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,    
        0x0b, 0x00, 0x03, 0x00,
          0x0a, 0x00, 0x00,
          0x0a, 0x01, 0x00,
          0x30
    };

    struct HeapVar code = {
        (const unsigned char*) bytecode,
        0x4e
    };

    struct HeapVar vers = version_c(code);

    if (vers.size > 3)  {
        printf("Erreur : %s\n", vers.pointer);
        return 1;
    }

    const unsigned char *v = vers.pointer;
    if (v[0] != 0 || v[1] != 1 || v[2] != 0)    {
        printf("Test échoué : version fausse.");
        return 1;
    }

    return 0;
}
